#2d Lists: It is a multidimensional form of a list

drinks = ['Coffee','Soda','Tea']
dinner = ['Pizza','Hamburger','Hotdog']
dessert = ['Cake','Ice Cream']

# We create new list which acts as a multi-dimensional list obtained by combining other lists
food = [drinks, dinner, dessert]

print(food[0])

# To access the element within the list
print(food[0][0])
print(food[1][0])
print(food[2][1])