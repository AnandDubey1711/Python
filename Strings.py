# Now shifting focus towards the string methods
name = 'Captain America'

# To get the length of the string we use len method
print(len(name))

# To check if a value exits in a string on not
print(name.find("C")) # Returns the first index of C - 0

# To capitalize the name
print(name.capitalize())

# To change into upper case upper is used
print(name.upper())

# To change into lower case lower is used
print(name.lower())

# To check if the a string contains only digit or not
print(name.isdigit())

# To check if the string contains only character or not
print(name.isalpha())

# To count the occurence of a value in a string count is used
print(name.count('a'))

